# IF statement

a = 20
if a > 10:
    print("a is greater than 10")


# IF-ELSE statement

b = 5
if b % 2 == 0:
    print("b is even")
else:
    print("b is odd")

# IF-ELIF-ELSE statement

c = 15
if c < 10:
    print("c is less than 10")
elif c < 20:
    print("c is between 10 and 20")
else:
    print("c is 20 or greater")
    